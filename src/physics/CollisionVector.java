package physics;

public class CollisionVector {

    private float startX;
    private float startY;
    private float endX;
    private float endY;

    public CollisionVector(float startX, float startY, float endX, float endY){
        this.startX = startX;
        this.startY = startY;
        this.endX = endX;
        this.endY = endY;
    }
}
